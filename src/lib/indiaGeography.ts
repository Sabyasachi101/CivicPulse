// Build the full STATE_CITY_WARD_MAP dynamically
export const STATE_CITY_WARD_MAP: { [state: string]: { [city: string]: string[] } } = {};

// Populate maps
for (const state of Object.keys(INDIAN_STATES_AND_UTS)) {
  STATE_CITY_WARD_MAP[state] = {};
  const cities = INDIAN_STATES_AND_UTS[state];
  for (const city of cities) {
    if (SPECIFIC_WARDS[state]?.[city]) {
      STATE_CITY_WARD_MAP[state][city] = SPECIFIC_WARDS[state][city];
    } else {
      STATE_CITY_WARD_MAP[state][city] = ["Ward 1 - Central", "Ward 2 - Sector Alpha", "Ward 3 - Sector Beta"];
    }
  }
}

// Flat list of all available wards in "State - City - Ward" format
export const WARDS_LIST: string[] = [];
for (const state of Object.keys(STATE_CITY_WARD_MAP)) {
  for (const city of Object.keys(STATE_CITY_WARD_MAP[state])) {
    for (const ward of STATE_CITY_WARD_MAP[state][city]) {
      WARDS_LIST.push(`${state} - ${city} - ${ward}`);
    }
  }
}

// Helper to parse "State - City - Ward" strings
export function parseWardString(wardStr: string) {
  const parts = (wardStr || "").split(" - ");
  return {
    state: parts[0] || "Delhi",
    city: parts[1] || "New Delhi",
    wardName: parts[2] || "Connaught Place"
  };
}
